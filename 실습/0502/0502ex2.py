for i in range(10):#반복조건
    print(i,end=" ")#반복조건이 참인동안 반복
print()
for i in range(1,10):#반복조건
    print(i,end=" ")
print()
for i in range(1,11,2):#반복조건
    print(i,end=" ")#홀수출력
print()
for i in range(10,0,-1):#반복조건
    print(i, end=" ")#역순 출력